		<div class="subTitle">Operating Performance</div>
        </div>
		<p>The Group continued to enjoy market  leadership in 2018. Telecommunications services revenue reached RMB670.9 billion and the Group has achieved a further enhancement to its revenue structure, where the respective proportions of revenues from its household market, corporate market and emerging business to its total revenue have remarkably increased. 4G customer base has recorded a net addition of 63.14 million to reach 713 million. Handset data traffic has risen by 182.1% and handset DOU has reached 3.6GB, maintaining burgeoning growth. The Group's broadband business has also reported a big leap, with the number of household broadband customers increasing by 34.2% to reach 147 million. The Group's corporate market has also demonstrated an upsurge in competitiveness, with the corporate customer base reaching 7.18 million subsequent to a net addition of 1.16 million. In the meantime, the IoT business witnessed notable growth with the number of IoT smart connections registering a net addition of 322 million to reach 551 million.</p>
		
		<div class="subTitleWrapper q">
           <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		<div class="subTitle">The "Four Growth Engines" Delivering Visible Results</div>
        </div>
		<div class="subSubTitle">The Personal Mobile Market</div>
        <p>In response to intense market competition, the Group has swiftly adjusted its operating strategy and seized the initiative, making full use of price elasticity. In addition to further enhancing its product portfolios to better match customer needs, the Group has adopted precision marketing and taken retention measures to targeted customers by making use of big data. These efforts have shown a beneficial effect on the Group's customer service level. In the fourth quarter of 2018, the market shares of the Group's 4G net-add customers and data traffic have both rallied to around 50%, suggesting that the Group has preliminarily reversed the market competitive trend in its favour and continued to maintain a leading position in the industry. In addition to effectively responding to market competition, the Group further ploughed on with its data business by launching innovative "data + contents + customer benefits" combo packages that spurred usage growth and rapidly boosted data traffic. In December 2018 alone, 4G handset customer DOU has increased by 171.1% year-on-year to exceed 6.6GB. The Group has also put a persistent effort to enhance high definition VoLTE (Voice over LTE) services and the number of VoLTE customers has rapidly increased to 380 million.</p>
		<div class="subSubTitle">The Household Market</div>
        <p>By consistently adopting a business development approach focusing on enhancing network speed, quality and value, the Group continued to boost broadband quality and crafted a high-quality brand image for this business line. Thanks to these efforts, the Group's household market has maintained a robust growth momentum in general. Household broadband customer base has recorded a net addition of 37.42 million to reach 147 million, of which, the proportion of customers subscribing to products with bandwidth of 100Mbps or above has increased by 45 percentage points year-on-year to reach 67%. To increase customer loyalty and value, the Group has also worked further on cultivating the digital family's ecology, strengthened broadband accessibility and forged ahead with the integrated development of an array of businesses including "Mobaihe" (a set-top box that provides high-definition video-on-demand service), "and-Mu" (a family surveillance camera) and "Smart gateway". The number of "Mobaihe" customers has reached 96.81 million subsequent to a net addition of 39.56 million, and accounted for 65.9% of the total number of broadband customers. Household broadband blended ARPU has risen by 3.2% year-on-year to reach RMB34.4.</p>
		<div class="subSubTitle">The Corporate Market</div>
        <p>As a move to extend its foothold in the blue-ocean corporate market, the Group has forged ahead with plans to further strengthen its corporate products and operations, with a special focus on key industry sectors. The Group's corporate market has demonstrated increased competiveness and its contribution to the Group's overall revenue growth has gone up further. The revenue market share of the Group's corporate telecommunications and informatisation service has increased by 2.2 percentage points to reach 38.5%. The revenues of the Group's key products, dedicated line services and IDC, recorded RMB18.03 billion and RMB7.25 billion respectively. In the meantime, the Group has also scaled up efforts to develop vertical markets. Specifically, more than 180 million pieces of equipment were connected with the Group's Industrial Internet cloud platform and a total of 83.95 million vehicles were connected with the Group's Internet of Vehicles network.</p>
		<div class="subSubTitle">The Emerging Business</div>
        <p>The Group has redoubled its efforts to develop an innovative business model and craft key products. Revenue from the Group's emerging business has posted a rapid increase and become a major contribution to the Group's overall revenue growth. The Group has also scaled up efforts to promote full-fledged product lines. Illustratively, FIFA World Cup viewership on "MIGU Video" has totalled 4.3 billion at all available viewing channels. At the same time, "and-Wallet" recorded a total annual transaction amount of more than RMB2.5 trillion. Additionally, spurred by the Group's initiatives to accelerate the development of IoT business, the number of IoT smart connections amounted to 551 million, of which, a total of 79.88 million were machine or equipment connections under the OneNET open platform. This massive scale has enabled OneNET to become one of the largest IoT platforms in the world in terms of the number of connections. Following measures with a view to expediting industry applications and proactively expanding cloud computing and big data businesses, revenue from ICT, cloud computing and big data businesses realised a robust expansion to RMB4.19 billion.</p>

		<div class="subTitleWrapper q">
           <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		<div class="subTitle">Continuously Improving Quality and Service</div>
        </div>
        
        <p>The Group realizes that quality is the lifeline for a telecommunications  operator and the ability to provide exceptional services constitutes a core competitive advantage that enables a company to achieve sustainable growth. While the Group has stepped up efforts to lift network quality for its 4G, household broadband, corporate markets in 2018, it continued to show unwavering devotion to providing exceptional customer services and place a relentless focus on its valued customers. The Group has redoubled its efforts to raise service standard and striven to establish itself as a telecommunications operator with long-lasting prestige and reputation.</p>                     
        <p>The Group has persistently taken measures to enhance customer perception. As to the mobile market, the download speed of its 4G network has shown a stable-to-rising development trend. In addition, a focus has been placed on handling customer complaints and customer satisfaction showed steady improvements. As to the broadband market, the Group has witnessed a continuous increase in the proportion of customers subscribing to products with high bandwidth and a persistent enhancement to customer perception on its household broadband services. Furthermore, the Group has set the seal on the standard service workflow that comprises installation, maintenance and service. The turnaround time for installing household broadband products and handling complaints has shortened by 13% and 15% respectively. As to international roaming services, the Group has provided roaming services in 260 locations and LTE roaming services in 181 locations around the world. As to telecommunications security, the Group is devoted to implementing measures to protect customer information security and privacy, and has proactively taken part in activities to curb new types of unlawful behaviours and crimes taken place in the telecommunications networks. It has also regularly performed assessments on customer information security and strengthened closed loop management on an ongoing basis, creating a healthy and safe telecommunications environment for customers.</p>
    <p>The Group has stepped up efforts to persistently enhance products and services. It has completely cancelled domestic data "roaming" tariff and endeavoured to promote large data packages. Innovative products which combine contents and customer benefits have been launched to better satisfy customer needs. The Group has also striven to rebrand "GoTone" and adopted a more sophisticated brand management strategy stressing customer segmentation, resulting in a reinforced sense of loyalty and sense of gain amongst customers. Meanwhile, the Group has undertaken further work to transform marketing channels, where more traditional services are rendered via intelligent, internet-based channels. A total of 62.3% of key business transactions are handled via electronic channels, up by 4.8 percentage points from last year. As an effort to promote experiential and interactive marketing and services, the Group has also assigned certain physical retail outlets to pilot marketing transformation by adopting the "new retail" model.</p>
        
		<div class="subTitleWrapper q">
           <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		<div class="subTitle">Strengthening Business Transformation</div>
        </div>
        <p>Taking into account the development needs of the "four growth engines", the Group has proactively sharpened its competences and persistently enhanced its long-term sustainability by taking a number of initiatives with a special focus on spreading the tenets of "centralised management, operational specialisation, market-oriented mechanism, lean organisation structure and process standardisation".</p>
        <p>Network capability has scaled new heights. With a total of 3.85 million mobile base stations (inclusive of 2.41 million
4G base stations), the Group boasted a 4G network covering more than 99% of the population in mainland China. The Group ranked first in the industry in terms of 4G coverage rate in urban areas, 4G overall coverage rate on High Speed Rail and successful VoLTE call connection rate. It also enjoyed an industry-leading 4G Customer Net Promoter Score and 4G network satisfaction rate. The Group's NB-IoT network has achieved continuous coverage in areas at township level and above across China. Meanwhile, the Group has also set its sights on further enhancing broadband network coverage and quality, where broadband network connection with at least 100Mbps bandwidth is now available for all household customers and FTTH penetration rate has reached 92%. The Group has also launched the "content leadership" campaign which championed the efficient deployment of CDN (Content Delivery Network) service nodes. CDN capacity built by the Group has increased by 63% and unified content distribution traffic has increased by 3.6 times. At the same time, the Group has expedited the planning and construction of the distribution networks for international and corporate dedicated lines, resulting in an appreciable increase in the capacity of distribution networks.
</p>
        <p>The Group continued to bolster internal core capabilities. It has taken a leading role in formulating the 5G network architecture standards and actively put forward proposals on 5G standard. To put this into perspective, the Group managed to capture the first place in the "network" category and the second place in the "wireless" category amongst global telecommunications operators in terms of the number of R15 proposals submitted. Serving important roles in a number of international organisations for standard formulation and owning a leading number of patents amongst global telecommunications operators in the international 4G patent pool, the Group has also increased its influence in the international information and communications discourse. In addition, the Group has fully capitalized on its research and development on artificial intelligence, with its intelligent robot "Yi Wa" serving the largest number of monthly customer service cases in the world. The Group has also left a mark on nurturing proprietary cloud computing and big data products. Attesting to this, the Group has scaled up efforts to internally penetrate their operational use and externally redraw their market boundary, and achieved the highest percentage of internally-developed resource pools and platforms in the industry. Meanwhile, the Group has taken actions to internally develop, centralise and optimise its overall IT capability, making a clear improvement in the response rate for IT support across the network.</p>
        <p>The Group continued to foster open co-operation. It has comprehensively launched the "1-3-9 Collaborative Plan" and constructed a well-rounded open platform that provides a broad avenue for participants to share their capabilities using online and offline channels, encouraging domestic and overseas industry chain players to explore the frontiers of innovation. The Group has also expedited measures that allow open access to existing fully-fledged service capabilities, in particular, the telecommunications capability open platform has served more than 150,000 companies, and the centralised certification platform has, on average, processed 670 million accreditations daily. By initiating the establishment of 5G Joint Innovation Industry Fund, the Group has facilitated end-to-end 5G industry adoption. With a view to strengthening strategic co-operation with external parties, the Group has entered into several strategic co-operation agreements with 11 local governments, 15 sizable enterprises and organizations, and achieved positive progress in more than 200 joint projects focusing on selected key areas.</p>
                      
		<div class="subTitleWrapper q">
           <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		<div class="subTitle">Continuously Enhancing Investment Efficiency</div>
        </div>
        <p>The Group is at a critical stage of development with a pressing need to undertake business transformation and build up network capability reserves. While the Group will focus on laying a solid foundation for the rapid growth of core businesses and securing market leadership in the next phase of development, it will remain committed to raising investment efficiency by sophisticated planning, targeted investments and sensible deployment of resources.</p>
        <p>Actual capital expenditure amounted to RMB167.1  billion for 2018,  with capital expenditure to service revenue ratio falling by 1.7 percentage points from 2017. Capital expenditure has effectively supported business development and the Group continued to see rising investment efficiency. Capital expenditure was spent primarily for the purposes of strengthening 4G network capacity in targeted areas, boosting broadband quality and speed, reinforcing the corporate market, bolstering transmission, improving IT support and so forth.</p>
        <p>In order to satisfy the needs arising from the growth of the "four growth engines" and the forthcoming network evolution, the Group plans to spend a total capital expenditure of RMB149.9 billion for 2019 (excluding 5G pre-commercial use), representing a decrease of 10.3% from 2018. The total capital expenditure for 2019 (including 5G pre-commercial use) will be less than the total capital expenditure for 2018. Capital expenditure will serve for a variety of purposes which primarily include catering for the growth of 4G data traffic, raising broadband quality and speed, securing investments for the corporate market, laying a solid foundation for the Group's transformational development and promoting network evolution and upgrades. The capital expenditure plan will be mainly supported by cash generated from operating activities. Central to the Group's overarching plan is to make investments that allow the Group to stay at the market forefront, foster innovation, ensure growth and enhance efficiency. The Group will continue to make targeted investments and refine its investment structure, with a view to satisfying business development needs and striving to continuously lift resource utilization efficiency.
</p>
		

