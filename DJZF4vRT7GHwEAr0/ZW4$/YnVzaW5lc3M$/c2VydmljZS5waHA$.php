        <p>China Mobile is an integrated telecommunications operator which provides full-service telecommunications services in Mainland China and Hong Kong Special Administrative Region.</p>

		<div class="subTitleWrapper q">
		   <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		   <div class="subTitle">Mobile Business</div>
		</div>
		<p>China Mobile provides the public with mobile communication services mainly via a 5G network based on a global unified standard, a 4G network based on the TDD/ FDD LTE standards, a 3G network based on the TD-SCDMA standard and a 2G network based on the GSM standard.</p>
        <p>Apart from local calls, our voice services also include international, Hong Kong, Macau and Taiwan long distance calls and roaming services. In addition, we also offer value-added services which include incoming call notifications, call waiting, conference calls and others.</p>
        <p>To meet our customers' surging demand for mobile internet, we offer wireless internet service as well as digital applications including music, video, reading, gaming and animation. While due attention has been paid to update speed and enrich applications, we have also continuously offered tariff discounts to our customers.</p>
        <p>Our commitment to excellence has kept us at the top of global telecom operators, covering the world's largest customer base.</p>
        
        <!--<p><img src="../images/business/service01.jpg" class="default_img"></p>-->
		
		<div class="subTitleWrapper q">
		   <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		   <div class="subTitle">Wireline Voice and Wireline Broadband Businesses</div>
		</div>
		<p>Riding on the opportunities brought about by the implementation of "Cyberpower" strategy by the Chinese government, we aims to develop our wireline broadband business with enhanced speed, quality and value, providing high-speed access with bandwidth of over 100M as standard. Placing a special focus on quality, we carefully craft our brand by devoting ourselves to offering premium products to our customers.</p>
        <p>We also provide wireline voice services which comprise local wireline telephone services and long distance wireline services via our wireline voice networks. Our long distance wireline services include domestic, international, Hong Kong, Macau and Taiwan long distance wireline services.</p>

		<div class="subTitleWrapper q">
		   <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		   <div class="subTitle">Corporate Customer Business</div>
		</div>
		<p>By focusing on key services such as dedicated line services, IDC and ICT, we strive to offer first-rate services to our corporate customers in a wide range of industry sectors including industry, agriculture, education, public administration, finance, transportation and healthcare.</p>
        <p>While we provide basic corporate communication products such as Corporate VPMN and SMS, we also tailor make various informatization solutions to cater for the specific needs of different industry sectors. For instance, specific applications and services are designed for the education, government, transportation, education administration and police sectors. At the same time, we are forging ahead with plans to enhance our corporate customer distribution system that is capable of "covering all market segments and maximizing synergy of all channels".</p>
        <p>Following its steady development, our corporate customer business has been obtaining an expanding market share year after year. With the ongoing rapid growth of revenue from dedicated line services, IDC and ICT, the revenue market share of our corporate telecommunications and informatization services has exceeded one third.</p>

		<div class="subTitleWrapper q">
		   <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		   <div class="subTitle">International Business</div>
		</div>
		<p>China Mobile provides a wide array of international telecommunications services which include IDD, roaming, Internet, MNC services and Value Added Business around the world.</p>
        <p>We have launched GSM international roaming service with 471 operators in 258 countries and regions, and data roaming service with 397 operators in 231 countries and regions. We have also offered a number of data roaming plans to meet customers’ needs for internet service while being abroad.</p>
        <p>For our multinational customers, we provide superior, one-stop services including IPLC, MPLS VPN, IP transit, CDN network, International SMS (I-SMS) and signaling through our extensive coverage of Point of Presence (POPs) in all four corners of the world.</p>

		<div class="subTitleWrapper q">
		   <div class="subTitleBg"><img src="../images/global/subtitleBg.png" alt=""/></div>
		   <div class="subTitle">Customer Services</div>
		</div>
		<p>At China Mobile, we adhere to the belief that "Customers are our priority, quality service is our principle" and continuously enhance our customer experience. We persistently respect and protect customer rights and proactively respond to customer requests, striving to serve our customers with high efficiency, convenience and quality.</p>
        <p>Available 24 hours per day, 7 days per week, our 10086 customer services hotline provides all-encompassing support to meet the diversified needs of our valued customers. Requests such as service enquiries and subscriptions, technical support, complaints & suggestions can be made via our hotline around the clock, in addition to our virtual outlets such as the Internet, WAP and Weixin. We also offer tailor-made services such as dedicated key account managers and door-to-door services to cater for the specific needs of our corporate customers.</p>
        <p>On top of enhancing service quality, we are also fully devoted to creating a secure communication platform for our customers by introducing highly resilient and fully protected features and implementing measures such as refining our service process, adopting the real-name registration system, intercepting fraud calls and fortifying information security.
</p>
		

