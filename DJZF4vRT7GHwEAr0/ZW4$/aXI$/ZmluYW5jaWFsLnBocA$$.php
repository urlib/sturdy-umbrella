            	<div class="a">
					<p>In 2018, the Company's operating revenue reached RMB736.8 million, down by 0.5% compared to the previous year, of which revenue from telecommunications services was RMB670.9 billion, up by 0.4% compared to the previous year. After applying the new revenue standard (IFRS/HKFRS 15) to the revenue figures of last year pursuant to a static calculation, the corresponding growth rate of operating revenue and telecommunications service revenue stood at 1.8% and 3.7%, respectively, on a comparable basis.</p>
					<p>The cancellation of domestic data "roaming" charges, which has been implemented since July 2018 in compliance with the state's requirements, has posed a significant impact on the Group's revenue. In addition, the increase in competition has led to a rapid decline in data value. The growth in the Group's telecommunications services revenue has been under tremendous pressure.</p>
