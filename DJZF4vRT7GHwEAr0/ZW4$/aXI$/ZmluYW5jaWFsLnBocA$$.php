            	<div class="a">
					<!--<p><img src="../images/ir/financial/2017_01.png" class="inlinePhoto"/></p>-->
					<div class="subSubTitle">Revenue from Voice Services</div>
					<p>Due to the substitution effect of mobile Internet, the cancellation of handset domestic long-distances roaming tariffs and other factors, revenue from voice services continued to decline to RMB108.1 billion, down by an ever- accelerating rate of 31.1% compared to the previous year, representing 16.1% of revenue from telecommunications services, down by 7.4 percentage points compared to the previous year.</p>
					<div class="subSubTitle">Revenue from Data Services</div>
					<p>Revenue from data services was RMB542.1 billion, up by 9.9% compared to the previous year, representing 80.8% of revenue from telecommunications services, up by 7.0 percentage points compared to the previous year. The Group's revenue structure was further optimized.</p>
					<p>As a result of the Group's continuous enrichment of its data products, enhancement of its precise marketing and deepening of its data traffic refined operation, data traffic business maintained a rapid growth. Revenue from wireless data traffic was RMB383.3 billion, up by 5.0% compared to the previous year, and was the significant engine of revenue growth. However, due to factors such as increasing competition and the cancellation of domestic data "roaming" charges, the revenue growth rate has declined. Wireless data traffic revenue as a proportion of revenue from telecommunications services rose to 57.1%. SMS/MMS services revenue was RMB28.8 billion, up by 2.6% compared to the previous year.</p>
                    <p>The Group has actively established high-quality broadband products, enhanced network service quality and enriched the contents and applications for the household market, thereby maintaining a strong growth in the number of broadband customers. Revenue from wireline broadband services reached RMB54.3 billion, up by 36.6% compared to the previous year, and became the significant source of growth for the Group's revenue.</p>
                    <p>The applications and information services made a breakthrough, with a rapid growth in dedicated lines, IDC, Internet of Things, "MIGU Video" and other businesses. Revenue from applications and information services was RMB75.7 billion, up by 24.8% compared to the previous year, representing a further enlarged scale of operation.</p>
