					<div class="subSubTitle">Leased Lines and Network Assets</div>
					<p>Leased lines and network assets expenses were RMB47.5 billion, up by 2.4% compared to the previous year and representing 6.5% of operating revenue. To maintain the Group's advantages in the quality and coverage of its networks, the towers leasing fee increased continuously to RMB39.0 billion, up by 5.5% compared to the previous year. The leasing fees for TD-SCDMA network capacity were RMB0.4 billion, down by 61.6% compared to the previous year. The leasing fees of "Village Connect" assets were RMB2.2 billion, down by 11.2% compared to the previous year.</p>
			
					<div class="subSubTitle">Interconnection</div>
					<p>Interconnection expenses were RMB20.7 billion, down by 4.9% compared to the previous year and representing 2.8% of operating revenue.</p>
			
					<div class="subSubTitle">Depreciation</div>
					<p>Depreciation was RMB152.5 billion, up by 1.8% compared to the previous year and representing 20.7% of operating revenue. The growth rate of depreciation has slowdown mainly because the Group has made provisions for the impairment of 2G wireless network equipment in the previous year.</p>
			
					<div class="subSubTitle">Employee Benefit and Related Expenses</div>
					<p>Employee benefit and related expenses were RMB93.9 billion, up by 9.9% compared to the previous year and representing 12.7% of operating revenue. The Group continued to adjust and optimize its personnel structure, and reallocate its compensation and incentives in favor of primary frontline employees, leading to an increase in employee benefit and related expenses.</p>
			
					<div class="subSubTitle">Selling Expenses</div>
					<p>Selling expenses were RMB60.3 billion, down by 1.2% compared to the previous year and representing 8.2% of operating revenue. The Group actively promoted the transformation of its marketing model, enhanced its precision marketing to customers, and endeavored to improve the efficiency of its utilization of marketing resources. The ratio of selling expenses to telecommunications services revenue remained industry-lowest.</p>
			
					<div class="subSubTitle">Cost of Products Sold</div>
					<p>Cost of products sold was RMB66.2 billion, down by 10.1% compared to the previous year. With the Group's promotion of the sale of handsets through open channels, cost of products sold decreased.</p>
			
					<div class="subSubTitle">Other Operating Expenses</div>
					<p>Other operating expenses were RMB174.2 billion, down by 4.4% compared to the previous year and representing 23.6% of operating revenue. Among these, maintenance expenses, operating lease charges and utilities expenses totaled RMB102.7 billion, up by 1.3% compared to the previous year, due mainly to the expansion of assets scale and increase in resources prices. In order to support network transformation, business innovation and implementation, the Group increased its expenses in operation support, research & development and related cost, which reached RMB44.0 billion, up by 15.7% compared to the previous year. Administrative expenses such as conference, office, travelling and business entertainment expenses were under strict control, remaining flat.</p>
				
